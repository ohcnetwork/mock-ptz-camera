// DrawText renders text onto an RGB24 frame buffer.
func DrawText(frame []byte, width, height, x, y int, text string, scale int, c color.RGBA) {
	charW := (fontW + 1) * scale // 5 pixels + 1 gap, scaled
	for i := 0; i < len(text); i++ {
		ch := text[i]
		if ch >= 128 {
			ch = '?'
		}
		glyph := glyphs[ch]
		drawGlyph(frame, width, height, x+i*charW, y, glyph, scale, c)
	}
}

func drawGlyph(frame []byte, width, height, gx, gy int, glyph [fontH]byte, scale int, c color.RGBA) {
	for row := 0; row < fontH; row++ {
		for col := 0; col < fontW; col++ {
			if glyph[row]&(1<<uint(4-col)) != 0 {
				for sy := 0; sy < scale; sy++ {
					for sx := 0; sx < scale; sx++ {
						px := gx + col*scale + sx
						py := gy + row*scale + sy
						if px >= 0 && px < width && py >= 0 && py < height {
							idx := (py*width + px) * 3
							frame[idx] = c.R
							frame[idx+1] = c.G
							frame[idx+2] = c.B
						}
					}
				}
			}
		}
	}
}

// DrawTextShadow renders text with a 1-pixel black shadow for readability.
func DrawTextShadow(frame []byte, width, height, x, y int, text string, scale int, fg color.RGBA) {
	shadow := color.RGBA{0, 0, 0, 255}
	DrawText(frame, width, height, x+1, y+1, text, scale, shadow)
	DrawText(frame, width, height, x, y, text, scale, fg)
}

// DarkenRect dims pixels in the given rectangle to create a background overlay.
func DarkenRect(frame []byte, width, height, rx, ry, rw, rh int) {
	for py := ry; py < ry+rh && py < height; py++ {
		if py < 0 {
			continue
		}
		for px := rx; px < rx+rw && px < width; px++ {
			if px < 0 {
				continue
			}
			idx := (py*width + px) * 3
			frame[idx] = frame[idx] >> 1
			frame[idx+1] = frame[idx+1] >> 1
			frame[idx+2] = frame[idx+2] >> 1
		}
	}
}
